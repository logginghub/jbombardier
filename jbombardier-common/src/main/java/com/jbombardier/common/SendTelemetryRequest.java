package com.jbombardier.common;

public class SendTelemetryRequest {

    private String ip;
    private int port;

    public SendTelemetryRequest() {}

    public String getIp() {
        return ip;
    }

    public int getPort() {
        return port;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public SendTelemetryRequest(String ip, int port) {
        this.ip = ip;
        this.port = port;

    }

}
